import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(new URL("..", import.meta.url).pathname);
const envLocalPath = resolve(root, ".env.local");
const dbPath = resolve(root, "db", "dev.db");

mkdirSync(resolve(root, "db"), { recursive: true });
if (!existsSync(dbPath)) writeFileSync(dbPath, "");

const lines = [
  `# Auto-generated by scripts/init-local-env.mjs`,
  `DATABASE_URL="file:${dbPath}"`,
  `DASHBOARD_PASSWORD=""`,
  `AGENT_TOKEN=""`,
  `DASHBOARD_URL="http://localhost:3000"`,
  `AGENT_ID="kolson-mac"`,
  `COMBINE_REPO_PATH=""`,
  ``,
];

// Preserve an existing password if already set.
if (existsSync(envLocalPath)) {
  const existing = readFileSync(envLocalPath, "utf8");
  const match = existing.match(/^\s*DASHBOARD_PASSWORD\s*=\s*"?([^"\n]*)"?\s*$/m);
  if (match?.[1]) lines[2] = `DASHBOARD_PASSWORD="${match[1]}"`;
  const tokenMatch = existing.match(/^\s*AGENT_TOKEN\s*=\s*"?([^"\n]*)"?\s*$/m);
  if (tokenMatch?.[1]) lines[3] = `AGENT_TOKEN="${tokenMatch[1]}"`;
  const urlMatch = existing.match(/^\s*DASHBOARD_URL\s*=\s*"?([^"\n]*)"?\s*$/m);
  if (urlMatch?.[1]) lines[4] = `DASHBOARD_URL="${urlMatch[1]}"`;
}

writeFileSync(envLocalPath, lines.join("\n"), "utf8");
console.log(`Wrote ${envLocalPath}`);
